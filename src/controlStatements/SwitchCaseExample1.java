package controlStatements;

public class SwitchCaseExample1 {
    public static void main(String[] args) {

        int a = 9;
        switch (a) {
            case 1:
                System.out.println("કારતક");
                break;
            case 2:
                System.out.println("માગશર");
                break;
            case 3:
                System.out.println("પોષ");
                break;
            case 4:
                System.out.println("મહા");
                break;
            case 5:
                System.out.println("ફાગણ");
                break;
            case 6:
                System.out.println("ચૈત્ર");
                break;
            case 7:
                System.out.println("વૈશાખ");
                break;
            case 8:
                System.out.println("જેઠ");
                break;
            case 9:
                System.out.println("અષાઢ");
                break;
            case 10:
                System.out.println("શ્રાવણ");
                break;
            case 11:
                System.out.println("ભાદરવો");
                break;
            case 12:
                System.out.println("આસો");
                break;
            default:
                System.out.println("Incorrect Input");

        }
    }
}
